from fourcolor import graph_is_4colorable

#Añadimos un nodo que se conecte a todos los nodos, así incrementamos
#estrictamente en 1 el grado de coloreabilidad del grafo; es decir,
#si el grado era 3, ahora será 4 y graph_is_4colorable devolverá true
#pero si justo era 4, incrementará a 5, haciendo que graph_is_4colorable
#devuelva false, esta transformación se da en tiempo polinómico, hay que
#recorrer solo una vez cada nodo del grafo, O(n)
def graph_is_3colorable(graph):
    add=[1]
    for i in graph:
        i.append(1)
        add.append(1)
    graph.append(add)
    return graph_is_4colorable(graph)
    



